/*
Hardware  connections, uses Port A
VCC 	- VBUS
GND 	- GND
CS 		- Pin 3(A) - SSI0Fss
RESET - Pin 7		 - GPIO
DC/RS - Pin 6		 - GPIO
MOSI	- Pin 5(A) - SSI0Tx
SCK		- Pin 2(A) - SSI0Clk
LED		- +3.3V
MISO	- Pin 4(A) - SSI0Rx
*/

/*
Abbreviation table
VCC	 			 - Voltage Common Collector
GND 			 - Ground (Duh)
CS 				 - Chip select (Low: Enabled, High: Disabled)
Reset			 - Chip reset signal (Low: Reset, High: Do nothing)
DC/RS			 - Data/Command signal (High: Command, Low: Data)
SDI (MOSI) - SPI Write data signal
SCK				 - Serial clock, the SPI bus clock signal
LED				 - The backlight control
SDO (MISO) - SPI Read data signal (Mostly for the integrated SD card slot) Ignore if not needed
*/

/* #define section */
